public class SwitchCaseDemo {
    public static void main(String[] args) {

        int sayı = 3;

        switch (sayı){
            case 1:
                System.out.println("Verilen Sayı 1 e eşit ");
                break;
            case 2:
                System.out.println("Verilen sayı 2 ye eşit");
                break;
            default:
                System.out.println("Sayı 1 e veya 2 ye eşit değil.");

        }

    }
}
